package Model;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

import java.io.Serializable;
import java.util.List;


public class NotificationData implements Serializable {

    @SerializedName("notification")
    @Expose
    private List<NotificationData> notification = null;

    public List<NotificationData> getNotification() {
        return notification;
    }

    public void setNotification(List<NotificationData> notification) {
        this.notification = notification;
    }


    @SerializedName("id")
    @Expose
    private Integer id;
    @SerializedName("empoyee_id")
    @Expose
    private String empoyeeId;
    @SerializedName("status")
    @Expose
    private String status;
    @SerializedName("title")
    @Expose
    private String title;
    @SerializedName("body")
    @Expose
    private String body;
    @SerializedName("created_at")
    @Expose
    private Object createdAt;
    @SerializedName("updated_at")
    @Expose
    private Object updatedAt;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getEmpoyeeId() {
        return empoyeeId;
    }

    public void setEmpoyeeId(String empoyeeId) {
        this.empoyeeId = empoyeeId;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public Object getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Object createdAt) {
        this.createdAt = createdAt;
    }

    public Object getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(Object updatedAt) {
        this.updatedAt = updatedAt;
    }
}
